package week3;

public class Q1 {
    public static void main(String[] args) {
        // use of +,-,*,/,%


        double num1=24.0;
        double num2=5.0;

        System.out.println("Adding num1 + num2= "+(num1+num2));
        System.out.println("Subtracting num1 - num2= "+(num1-num2));
        System.out.println("Multiplying num1 * num2= "+(num1*num2));
        System.out.println("Division of num1 by num2= "+(num1/num2));
        System.out.println("Remainder num1 + num2= "+(num1%num2));


    }
}
