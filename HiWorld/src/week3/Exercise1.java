//Computing the area of a circle
public class Exercise1 
{
	/** Main Method */

	public static void main(String[] args) 
	{
	double radius;
	double area;
	
	//Assign a radius
	radius = 20;
	
	//Compute Area
	area= radius * radius * 3.14159;
			
	//Dispaly results
	System.out.println("The area for the circle of radius "+radius+" is "+ area);

	}

}
